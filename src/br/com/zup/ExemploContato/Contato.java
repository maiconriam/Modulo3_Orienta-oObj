package br.com.zup.ExemploContato;

public class Contato {
    String nome;
    String telefone;
    String email;
    int idade;

    public Contato(){

    }
    public Contato(String nome, String telefone, String email, int idade){
        this.nome = nome;
        this.telefone = telefone;
        this.email = email;
        this.idade = idade;
    }
}
